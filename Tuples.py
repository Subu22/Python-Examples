# Lists: Mutable
list1 = ['History' , 'Comp_Sci', 'Math', 'Physics']
list2 = list1

print(list1)
print(list2)

list1[0] = 'Art'

print(list1)
print(list2)

# Tuples: Immutable

tuple1  = ('History' , 'Comp_Sci', 'Math', 'Physics')
tuple2 = tuple1

print(tuple1)
print (tuple2)

tuple1[0] = 'Art'

print(tuple1)
print (tuple2)
